    public static String getScreenType(String str) {
        if (str.equals("Home Screen") || str.equals(AREASELECTIONSCREEN) || str.equals(CIYTSELECTIONSCREEN)) {
            return "home";
        }
        if (str.equals(SPLASHSCREEN)) {
            return SCREEN_TYPE_LANDING;
        }
        if (str.equals("Restaurant List Screen") || str.equals(FILTER_SCREEN) || str.equals(CUISINE_SELECTION_SCREEN)) {
            return "shop_list";
        }
        if (str.equals("")) {
            return "other";
        }
        if (str.equals(RESTAURANTMENU) || str.equals(ITEM_DETAILS_SCREEN) || str.equals(FREE_ITEM_LIST)) {
            return "shop_details";
        }
        if (str.equals("Cart Screen")) {
            return "cart";
        }
        if (str.equals("Checkout Screen")) {
            return "checkout";
        }
        if (str.equals(ORDER_CONFORMATION)) {
            return SCREEN_TYPE_ORDERCONFIRM;
        }
        if (str.equals(GROCERY_DETAILS_SCREEN) || str.equals(GROCERYMENUSCREEN) || str.equals(GROCERYMENU_SEARCH)) {
            return "shop_details";
        }
        if (str.equals(ORDERS_LIST) || str.equals(REGISTRATION) || str.equals(VERIFY_ACCOUNT_INFO) || str.equals(ADD_ADDRESS) || str.equals(EDIT_ADDRESS) || str.equals(LOGIN_SCREEN) || str.equals("Forgot Password Screen")) {
            return SCREEN_TYPE_USER_ACCOUNT;
        }
        if (str.equals(FEEDBACK_SCREEN)) {
            return SCREEN_TYPE_COMPANY;
        }
        if (str.equals(TRACKORDER_INTERMEDIATE_SCREEN) || str.equals(TRACKORDER_WEBVIEW_SCREEN)) {
            return "track_order";
        }
        if (str.equals(CHECKOUT_DOT_COM_SCREEN_CREDIT) || str.equals(PAYMENT_WEBVIEW_SCREEN) || str.equals(CHECKOUT_DOT_COM_SCREEN_DEBIT)) {
            return "payment";
        }
        if (str.equals(BUSY_POPUP)) {
            return SCREEN_TYPE_BUSY_POPUP;
        }
        if (str.equals(NO_ITEMS_BOTTOMSHEET)) {
            return SCREEN_TYPE_NO_ITEMS_BOTTOMSHEET;
        }
        return "other";
    }
